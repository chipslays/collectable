            if ($maxValue === $sentinel || $value > $maxValue) {
                $maxValue = $value;
                $maxItem  = $item;
            }
        }

        return $maxItem;
    }

    /**
     * Return the median value, or the median of the plucked path values.
     * Returns null for an empty set.
     *
     * For even-length sets the average of the two middle values is returned.
     */
    public function median(?string $path = null): float|null
    {
        $values = $path !== null
            ? array_values($this->pluck($path)->all())
            : array_values($this->items);

        $count = count($values);

        if ($count === 0) {
            return null;
        }

        sort($values);
        $mid = (int) floor($count / 2);

        return (float) ($count % 2 === 0
            ? ($values[$mid - 1] + $values[$mid]) / 2
            : $values[$mid]);
    }

    /**
     * Return the statistical mode(s) — the most frequently occurring value(s).
     *
     * Returns a Collection of the most common values (multiple entries when tied).
     * Returns an empty Collection for an empty set.
     *
     * @example mode()          // top-level scalars
     * @example mode('score')   // most common score across items
     */
    public function mode(?string $path = null): static
    {
        $values = $path !== null ? $this->pluck($path)->all() : $this->items;

        if ($values === []) {
            return new static([], $this->wildcard, $this->delimiter);
        }

        $counts = [];
        foreach ($values as $v) {
            $key          = serialize($v);
            $counts[$key] = ($counts[$key] ?? 0) + 1;
        }

        $max    = max($counts);
        $result = [];
        $seen   = [];

        foreach ($values as $v) {
            $key = serialize($v);
            if ($counts[$key] === $max && !isset($seen[$key])) {
                $result[] = $v;
                $seen[$key] = true;
            }
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Return the population standard deviation, or the std-dev of the plucked path values.
     * Returns null for a set with fewer than 2 values.
     *
     * Uses population std-dev (divides by n, not n-1).
     * Pass $sample = true to get the sample std-dev (divides by n-1).
     *
     * @example standardDeviation()           // top-level scalars
     * @example standardDeviation('score')    // std-dev of scores
     * @example standardDeviation('price', sample: true)  // sample std-dev
     */
    public function standardDeviation(?string $path = null, bool $sample = false): float|null
    {
        $values = $path !== null
            ? array_values($this->pluck($path)->all())
            : array_values($this->items);

        $count = count($values);

        if ($count < 2) {
            return null;
        }

        $mean     = array_sum($values) / $count;
        $variance = array_sum(array_map(fn($v) => ($v - $mean) ** 2, $values))
            / ($sample ? $count - 1 : $count);

        return sqrt($variance);
    }

    /**
     * Return the percentage of items passing $callback (0–100.0).
     *
     * @example percentage(fn($u) => $u['active'])
     * @example percentage(fn($u) => $u['score'] >= 50, precision: 1)
     */
    public function percentage(callable $callback, int $precision = 2): float
    {
        $total = count($this->items);

        if ($total === 0) {
            return 0.0;
        }

        $passing = 0;
        foreach ($this->items as $key => $item) {
            if ($callback($item, $key)) {
                $passing++;
            }
        }

        return round($passing / $total * 100, $precision);
    }

    /**
     * Count occurrences per group value.
     *
     * Without $by, counts occurrences of each scalar value in the collection.
     * With a dot-notation path or callback, groups and counts by that key.
     *
     * @example countBy()           // ['a' => 2, 'b' => 3]  (top-level scalars)
     * @example countBy('role')     // ['admin' => 1, 'editor' => 2]
     * @example countBy(fn($u) => $u['active'] ? 'on' : 'off')
     */
    public function countBy(string|callable|null $by = null): static
    {
        $sentinel = self::sentinel();

        if ($by === null) {
            $counts = [];
            foreach ($this->items as $item) {
                $k          = is_scalar($item) ? (string) $item : serialize($item);
                $counts[$k] = ($counts[$k] ?? 0) + 1;
            }

            return new static($counts, $this->wildcard, $this->delimiter);
        }

        $callback = is_string($by)
            ? fn($item) => is_array($item)
                ? $this->resolveGet($item, $this->parsePath($by), $sentinel)
                : $sentinel
            : $by;

        $counts = [];
        foreach ($this->items as $key => $item) {
            $group = $callback($item, $key);
            if ($group === $sentinel) {
                continue;
            }
            $k          = is_scalar($group) ? (string) $group : serialize($group);
            $counts[$k] = ($counts[$k] ?? 0) + 1;
        }

        return new static($counts, $this->wildcard, $this->delimiter);
    }

    /**
     * Return a new Collection containing only the specified top-level keys.
     *
     * @example only(['id', 'name'])
     */
    public function only(array $keys): static
    {
        return new static(
            array_intersect_key($this->items, array_flip($keys)),
            $this->wildcard,
            $this->delimiter,
        );
    }

    /**
     * Pick multiple fields from each item, returning re-shaped sub-arrays.
     *
     * Supports dot-notation paths. The result keys mirror the given paths.
     *
     * @example select(['id', 'name'])
     * @example select(['id', 'user.email'])
     */
    public function select(array $fields): static
    {
        $sentinel = self::sentinel();
        $result   = [];

        foreach ($this->items as $item) {
            if (!is_array($item)) {
                $result[] = $item;
                continue;
            }

            $row = [];
            foreach ($fields as $field) {
                $segments = $this->parsePath($field);
                $v        = $this->resolveGet($item, $segments, $sentinel);
                if ($v !== $sentinel) {
                    $this->resolveSet($row, $segments, $v);
                }
            }
            $result[] = $row;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
