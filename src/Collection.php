     * Returns a Collection of arrays, each containing the i-th element from
     * each source. Missing elements are filled with null.
     *
     * @example Collection::make([1, 2, 3])->zip(['a', 'b', 'c'])
     *          // [[1, 'a'], [2, 'b'], [3, 'c']]
     */
    public function zip(array ...$arrays): static
    {
        $values = array_values($this->items);
        $length = max(count($values), ...array_map('count', $arrays ?: [[]]));
        $result = [];

        for ($i = 0; $i < $length; $i++) {
            $entry = [$values[$i] ?? null];
            foreach ($arrays as $arr) {
                $entry[] = $arr[$i] ?? null;
            }
            $result[] = $entry;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Unzip a collection of tuples into separate collections (inverse of zip).
     *
     * Each item should be an array (or tuple). Returns an array of Collections,
     * where the i-th Collection contains all i-th elements from each item.
     * Missing positions are filled with null.
     *
     * @example Collection::make([[1,'a'],[2,'b']])->unzip()
     *          // [Collection[1,2], Collection['a','b']]
     */
    public function unzip(): array
    {
        if ($this->items === []) {
            return [];
        }

        $width  = max(array_map(fn($item) => is_array($item) ? count($item) : 1, $this->items));
        $result = array_fill(0, $width, []);

        foreach ($this->items as $tuple) {
            $tuple = is_array($tuple) ? array_values($tuple) : [$tuple];
            for ($i = 0; $i < $width; $i++) {
                $result[$i][] = $tuple[$i] ?? null;
            }
        }

        return array_map(
            fn($col) => new static($col, $this->wildcard, $this->delimiter),
            $result,
        );
    }

    /**
     * Remove and return the last item.
     */
    public function pop(): mixed
    {
        return array_pop($this->items);
    }

    /**
     * Remove and return the first item.
     */
    public function shift(): mixed
    {
        return array_shift($this->items);
    }

    /**
     * Add $value to the beginning of the collection.
     *
     * If $key is given it is used as the associative key; otherwise
     * the item is prepended with a numeric index via array_unshift().
     */
    public function prepend(mixed $value, mixed $key = null): static
    {
        if ($key === null) {
            array_unshift($this->items, $value);
        } else {
            $this->items = [$key => $value] + $this->items;
        }

        return $this;
    }

    /**
     * Insert $value at the given integer $index, shifting subsequent items.
     *
     * - Negative indices count from the end: -1 inserts before the last item.
     * - An $index beyond the collection length appends at the end.
     * - Keys are reset to consecutive integers after insertion.
     *
     * Returns a new collection (non-mutating).
     *
     * @example insert(0, 'first')  // prepend
     * @example insert(2, 'x')     // inject at position 2
     * @example insert(-1, 'x')    // before last item
     */
    public function insert(int $index, mixed $value): static
    {
        $items = array_values($this->items);
        $count = count($items);

        if ($index < 0) {
            $index = max(0, $count + $index);
        } else {
            $index = min($index, $count);
        }

        array_splice($items, $index, 0, [$value]);

        return new static($items, $this->wildcard, $this->delimiter);
    }

    /**
     * Retrieve the value at $path and then remove it from the collection.
     *
     * Returns $default if the path does not exist.
     *
     * @example pull('user.token')
     */
    public function pull(string $path, mixed $default = null): mixed
    {
        $value = $this->get($path, $default);
        $this->remove($path);

        return $value;
    }

    /**
     * Remove and return items starting at $offset.
     *
     * Modifies the collection in-place. Returns the removed items as a new Collection.
     *
     * @param int        $offset       Start index (negative counts from end).
     * @param int|null   $length       Number of items to remove (null = to end).
     * @param array      $replacement  Items to insert in place of the removed ones.
     *
     * @example splice(2)             // removes from index 2 to end
     * @example splice(1, 2)          // removes 2 items at index 1
     * @example splice(1, 2, ['x'])   // removes 2 items and inserts 'x'
     */
    public function splice(int $offset, ?int $length = null, array $replacement = []): static
    {
        $removed = $length === null
            ? array_splice($this->items, $offset, replacement: $replacement)
            : array_splice($this->items, $offset, $length, $replacement);

        return new static($removed, $this->wildcard, $this->delimiter);
    }

    /**
     * Iterate over every item with $callback($value, $key).
     *
     * Return false from the callback to stop early.
     */
    public function each(callable $callback): static
    {
        foreach ($this->items as $key => $value) {
            if ($callback($value, $key) === false) {
                break;
            }
        }

        return $this;
    }

    /**
     * Take items until the callback returns true (or the value is found).
     *
     * @example takeUntil(fn($v) => $v > 3)  // [1, 2, 3]
     * @example takeUntil(4)                  // [1, 2, 3]
     */
    public function takeUntil(mixed $valueOrCallback): static
    {
        $result = [];

        foreach ($this->items as $key => $item) {
            $done = is_callable($valueOrCallback)
                ? (bool) $valueOrCallback($item, $key)
                : ($item === $valueOrCallback);

            if ($done) {
                break;
            }

            $result[] = $item;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Take items while the callback returns true (or value matches).
     *
     * @example takeWhile(fn($v) => $v < 4)  // [1, 2, 3]
     */
    public function takeWhile(mixed $valueOrCallback): static
    {
        $result = [];

        foreach ($this->items as $key => $item) {
            $cont = is_callable($valueOrCallback)
                ? (bool) $valueOrCallback($item, $key)
                : ($item === $valueOrCallback);

            if (!$cont) {
                break;
            }

            $result[] = $item;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Skip items until the callback returns true (or value is found),
     * then return the rest including the matching item.
     *
     * @example skipUntil(fn($v) => $v >= 3)  // [3, 4, 5]
     * @example skipUntil(3)                   // [3, 4, 5]
     */
    public function skipUntil(mixed $valueOrCallback): static
    {
        $result = [];
        $found  = false;

        foreach ($this->items as $key => $item) {
            if (!$found) {
                $match = is_callable($valueOrCallback)
                    ? (bool) $valueOrCallback($item, $key)
                    : ($item === $valueOrCallback);

                if (!$match) {
                    continue;
                }

                $found = true;
            }

            $result[] = $item;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Skip items while the callback returns true (or value matches),
     * then return the rest.
     *
     * @example skipWhile(fn($v) => $v < 3)  // [3, 4, 5]
     */
    public function skipWhile(mixed $valueOrCallback): static
    {
        $result   = [];
        $skipping = true;

        foreach ($this->items as $key => $item) {
            if ($skipping) {
                $skip = is_callable($valueOrCallback)
                    ? (bool) $valueOrCallback($item, $key)
                    : ($item === $valueOrCallback);

                if ($skip) {
                    continue;
                }

                $skipping = false;
            }

            $result[] = $item;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Reduce the collection to a single value via $callback($carry, $value).
     */
    public function reduce(callable $callback, mixed $initial = null): mixed
    {
        return array_reduce($this->items, $callback, $initial);
    }

