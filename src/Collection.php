    /**
     * Split the collection into two: items passing $callback and items failing it.
     *
     * Returns [Collection $passing, Collection $failing].
     *
     * @example [$admins, $rest] = $users->partition(fn($u) => $u['role'] === 'admin')
     */
    public function partition(callable $callback): array
    {
        $pass = [];
        $fail = [];

        foreach ($this->items as $key => $item) {
            if ($callback($item, $key)) {
                $pass[$key] = $item;
            } else {
                $fail[$key] = $item;
            }
        }

        return [
            new static($pass, $this->wildcard, $this->delimiter),
            new static($fail, $this->wildcard, $this->delimiter),
        ];
    }

    /**
