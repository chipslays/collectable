    /**
     * Check whether the collection contains the given value, or at least one
     * item satisfies the callback.
     *
     * Two-arg path form: contains('role', 'admin') — checks whether any item
     * has that dot-notation path equal to that value (strict comparison).
     *
     * @example contains('Alice')
     * @example contains('role', 'admin')
     * @example contains(fn($v) => $v > 10)
     */
    public function contains(mixed $pathOrValueOrCallback, mixed $value = null): bool
    {
        if (is_string($pathOrValueOrCallback) && func_num_args() >= 2) {
            $sentinel = self::sentinel();
            $segments = $this->parsePath($pathOrValueOrCallback);
            foreach ($this->items as $item) {
                if (!is_array($item)) {
                    continue;
                }
                $found = $this->resolveGet($item, $segments, $sentinel);
                if ($found !== $sentinel && $found === $value) {
                    return true;
                }
            }

            return false;
        }

        if (is_callable($pathOrValueOrCallback)) {
            foreach ($this->items as $key => $val) {
                if ($pathOrValueOrCallback($val, $key)) {
                    return true;
                }
            }

            return false;
        }

        return in_array($pathOrValueOrCallback, $this->items, strict: true);
    }

    /**
     * Determine whether the collection does NOT contain the given value/path/callback.
     *
     * Inverse of contains(). Accepts the same argument forms.
