    /**
     * Return the item with the minimum value at the given path or via a callback.
     *
     * Unlike min() which returns the raw scalar, minBy() returns the whole item.
     * Returns null for an empty collection.
     *
     * @example minBy('price')                         // item with lowest price
     * @example minBy(fn($i) => $i['w'] * $i['h'])    // item with smallest area
     */
    public function minBy(string|callable $by): mixed
    {
        if ($this->items === []) {
            return null;
        }

        $sentinel = self::sentinel();
        $callback = is_string($by)
            ? fn($item) => is_array($item)
                ? $this->resolveGet($item, $this->parsePath($by), $sentinel)
                : $sentinel
            : $by;

        $minItem  = null;
        $minValue = $sentinel;

        foreach ($this->items as $key => $item) {
            $value = $callback($item, $key);
            if ($value === $sentinel) {
                continue;
            }
            if ($minValue === $sentinel || $value < $minValue) {
                $minValue = $value;
                $minItem  = $item;
            }
        }

        return $minItem;
    }

    /**
     * Return the item with the maximum value at the given path or via a callback.
     *
     * Unlike max() which returns the raw scalar, maxBy() returns the whole item.
     * Returns null for an empty collection.
     *
     * @example maxBy('score')                          // item with highest score
     * @example maxBy(fn($i) => $i['w'] * $i['h'])     // item with largest area
     */
    public function maxBy(string|callable $by): mixed
    {
        if ($this->items === []) {
            return null;
        }

        $sentinel = self::sentinel();
        $callback = is_string($by)
            ? fn($item) => is_array($item)
                ? $this->resolveGet($item, $this->parsePath($by), $sentinel)
                : $sentinel
            : $by;

        $maxItem  = null;
        $maxValue = $sentinel;

        foreach ($this->items as $key => $item) {
            $value = $callback($item, $key);
            if ($value === $sentinel) {
                continue;
