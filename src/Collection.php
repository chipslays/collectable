    /**
     * Return a new Collection with duplicate items removed.
     *
     * Accepts an optional dot-notation path or callback to determine the
     * uniqueness key. Result is always re-indexed.
     *
     * @example unique()
     * @example unique('email')
     * @example unique(fn($item) => $item['domain'])
     */
    public function unique(string|callable|null $by = null): static
    {
        if ($by === null) {
            return new static(
                array_values(array_unique($this->items, SORT_REGULAR)),
                $this->wildcard,
                $this->delimiter,
            );
        }

        $sentinel = self::sentinel();
        $callback = is_string($by)
            ? fn($item) => is_array($item)
                ? $this->resolveGet($item, $this->parsePath($by), $sentinel)
                : $item
            : $by;

        $seen   = [];
        $result = [];

        foreach ($this->items as $key => $item) {
            $hash = serialize($callback($item, $key));

            if (!array_key_exists($hash, $seen)) {
                $seen[$hash]  = true;
                $result[$key] = $item;
            }
        }

        return new static(array_values($result), $this->wildcard, $this->delimiter);
    }

    /**
     * Return unique items using strict (===) comparison.
     *
     * Unlike unique(), 1 and '1' are treated as different values.
     * Accepts the same $by forms as unique().
     *
     * @example uniqueStrict()           // top-level strict dedup
     * @example uniqueStrict('status')   // dedup by path, strict
     */
    public function uniqueStrict(string|callable|null $by = null): static
    {
        $sentinel = self::sentinel();

        $callback = $by === null
            ? null
            : (is_string($by)
                ? fn($item) => is_array($item)
                    ? $this->resolveGet($item, $this->parsePath($by), $sentinel)
                    : $item
                : $by);

        $seen   = [];
        $result = [];

        foreach ($this->items as $key => $item) {
            $val = $callback !== null ? $callback($item, $key) : $item;

            $isDup = false;
            foreach ($seen as $s) {
                if ($s === $val) {
                    $isDup = true;
                    break;
                }
            }

            if (!$isDup) {
                $seen[]       = $val;
                $result[$key] = $item;
            }
        }

        return new static(array_values($result), $this->wildcard, $this->delimiter);
    }

