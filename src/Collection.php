    /**
     * Split a dot-notation path into segments.
     *
     * @return non-empty-list<string>
     */
    private function parsePath(string $path): array
    {
        return explode($this->delimiter, $path);
    }

    /**
     * Lazy sentinel singleton used to distinguish "not found" from null values.
     */
    private static function sentinel(): object
    {
        static $sentinel = null;

        return $sentinel ??= new \stdClass();
    }

    /**
     * Recursively resolve a get operation.
     *
     * - Plain segment  -> descend into the matching key
     * - Wildcard       -> map over every key, recurse, preserve keys -> nested result
     *
     * Returns the $sentinel object when the path cannot be resolved.
     */
    private function resolveGet(mixed $data, array $segments, object $sentinel): mixed
    {
        if ($segments === []) {
            return $data;
        }

        if (!is_array($data)) {
            return $sentinel;
        }

        $segment = array_shift($segments);
        [$bare, $keyPath] = $this->parseSegment($segment);

        if ($bare === $this->wildcard) {
            $result = [];
            foreach ($data as $index => $child) {
                $value = $this->resolveGet($child, $segments, $sentinel);
                if ($value !== $sentinel) {
                    if ($keyPath !== null && is_array($child)) {
                        $k = $this->resolveGet($child, $this->parsePath($keyPath), $sentinel);
                        $result[$k !== $sentinel && is_scalar($k) ? $k : $index] = $value;
                    } else {
                        $result[$index] = $value;
                    }
                }
            }
            return $result === [] && $segments !== [] ? $sentinel : $result;
        }

        if (!array_key_exists($segment, $data)) {
            return $sentinel;
        }

        return $this->resolveGet($data[$segment], $segments, $sentinel);
    }

    /**
     * Parse a path segment for wildcards and key extraction.
     */
    private function parseSegment(string $segment): array
    {
        if (preg_match('/^\*\[(.+)]$/', $segment, $m)) {
            return [$this->wildcard, $m[1]];
        }
        return [$segment, null];
    }

    /**
     * Recursively resolve a set operation.
     *
     * Creates intermediate arrays when they do not exist.
     * Wildcard -> applies to every matching child node.
     */
    private function resolveSet(array &$data, array $segments, mixed $value): void
    {
        $segment = array_shift($segments);

        if ($segment === $this->wildcard) {
            foreach ($data as &$child) {
                if ($segments === []) {
                    $child = $value;
                } elseif (is_array($child)) {
                    $this->resolveSet($child, $segments, $value);
                }
            }
            unset($child);
            return;
        }

        if ($segments === []) {
            $data[$segment] = $value;
            return;
        }

        if (!isset($data[$segment]) || !is_array($data[$segment])) {
            $data[$segment] = [];
        }

        $this->resolveSet($data[$segment], $segments, $value);
    }

    /**
     * Recursively resolve a remove operation.
     *
     * Wildcard -> unsets the final key in every matching child node.
     */
    private function resolveRemove(array &$data, array $segments): void
    {
        $segment = array_shift($segments);

        if ($segment === $this->wildcard) {
            foreach ($data as &$child) {
                if ($segments === []) {
                    // wildcard as final segment: clear entire current level
                    $data = [];
                    return;
                }
                if (is_array($child)) {
                    $this->resolveRemove($child, $segments);
                }
            }
            unset($child);
            return;
        }

        if ($segments === []) {
            unset($data[$segment]);
            return;
        }

        if (isset($data[$segment]) && is_array($data[$segment])) {
            $this->resolveRemove($data[$segment], $segments);
        }
    }

    /**
     * Recursively check whether a path exists AND the value is not null.
     *
     * Wildcard -> true if at least one child resolves to a non-null value.
     * Uses array_find() (PHP 8.4) for wildcard existence checks.
     */
    private function resolveHas(mixed $data, array $segments, object $sentinel): bool
    {
        if ($segments === []) {
            return $data !== null && $data !== $sentinel;
        }

        if (!is_array($data)) {
            return false;
        }

        $segment = array_shift($segments);

        if ($segment === $this->wildcard) {
            return array_find($data, fn($child) => $this->resolveHas($child, $segments, $sentinel)) !== null;
        }

        if (!array_key_exists($segment, $data)) {
            return false;
        }

        return $this->resolveHas($data[$segment], $segments, $sentinel);
    }

    /**
     * Recursively check whether a path key exists (value may be null).
     *
     * Wildcard -> true if at least one child has the key chain.
     */
    private function resolveHasKey(mixed $data, array $segments, object $sentinel): bool
    {
        if ($segments === []) {
            return $data !== $sentinel;
        }

        if (!is_array($data)) {
            return false;
        }

        $segment = array_shift($segments);

        if ($segment === $this->wildcard) {
            return array_find($data, fn($child) => $this->resolveHasKey($child, $segments, $sentinel)) !== null;
        }

        if (!array_key_exists($segment, $data)) {
            return false;
        }

        return $this->resolveHasKey($data[$segment], $segments, $sentinel);
    }

    /**
     * Recursively collapse a nested array into a flat indexed list up to $depth levels.
     */
    private function flattenValues(array $array, int|float $depth): array
    {
        $result = [];
        foreach ($array as $item) {
            if ($item instanceof self) {
                $item = $item->all();
            }
            if (is_array($item) && $depth > 0) {
                array_push($result, ...$this->flattenValues($item, $depth - 1));
            } else {
                $result[] = $item;
            }
        }
        return $result;
    }

    /**
     * Recursively flatten a nested array into dot-notation key => value pairs.
     */
    private function flattenArray(array $array, string $prefix): array
    {
        $result = [];
        foreach ($array as $key => $value) {
            $fullKey = $prefix !== '' ? $prefix . $this->delimiter . $key : (string) $key;
            if (is_array($value)) {
                $result += $this->flattenArray($value, $fullKey);
            } else {
                $result[$fullKey] = $value;
            }
        }
        return $result;
    }
