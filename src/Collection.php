<?php

declare(strict_types=1);

namespace Collectable;

use ArrayAccess;
use ArrayIterator;
use Collectable\Concerns\Macroable;
use Collectable\Contracts\Arrayable;
use Collectable\Contracts\Collectable;
use IteratorAggregate;

/**
 * A flexible collection with dot-notation and wildcard path support.
 *
 * Supports single-level access:  get('user.name')
 * Supports wildcard access:      get('users.*.name')
 * Supports nested wildcards:     get('users.*.emails.*.address')
 *
 * Wildcard results preserve original keys as a nested structure,
 * so you always know which data belongs to which parent.
 *
 * @template-implements ArrayAccess<string, mixed>
 * @template-implements IteratorAggregate<string|int, mixed>
 */
class Collection implements Collectable
{
    use Macroable;

    public function __construct(
        protected array $items = [],
        public readonly string $wildcard = '*',
        public readonly string $delimiter = '.'
    ) {}

    /**
     * Create a new collection from an array.
     */
    public static function make(array $items = [], string $wildcard = '*', string $delimiter = '.'): static
    {
        return new static($items, $wildcard, $delimiter);
    }

    /**
     * Retrieve a value by dot-notation path and wrap the result in a new Collection.
     *
     * Useful for wildcard paths that return nested arrays — instead of wrapping
     * the result manually you get a Collection ready for further chaining.
     *
     * @example collect('users.*.emails.*.address')->flatten()->values()->all()
     * @example collect('roles')->where('active', true)
     */
    public function collect(?string $path = null, mixed $default = null): static
    {
        return new static(
            (array) $this->get($path, $default),
            $this->wildcard,
            $this->delimiter,
        );
    }

    /**
     * Wrap a value into a Collection.
     *
     * - Collection -> returned as-is
     * - array      -> used directly
     * - anything else -> wrapped in a single-element array
     */
    public static function wrap(mixed $value): static
    {
        if ($value instanceof static) {
            return $value;
        }

        return new static(is_array($value) ? $value : [$value]);
    }

    /**
     * Create a Collection of $n items generated by $callback($i), where $i goes 1…$n.
     *
     * @example times(5, fn($i) => $i * 2) // [2, 4, 6, 8, 10]
     */
    public static function times(int $n, callable $callback): static
    {
        if ($n <= 0) {
            return new static();
        }

        return new static(array_map($callback, range(1, $n)));
    }

    /**
     * Create a Collection of numbers from $from to $to with optional $step.
     *
     * @example range(1, 5)        // [1, 2, 3, 4, 5]
     * @example range(0, 1, 0.25)  // [0, 0.25, 0.5, 0.75, 1.0]
     */
    public static function range(int|float $from, int|float $to, int|float $step = 1): static
    {
        return new static(range($from, $to, $step));
    }

    /**
     * Unwrap a Collection or array into a plain PHP array.
     *
     * Counterpart to wrap() — always returns a plain array.
     *
     * @example unwrap($collection)  // same as $collection->all()
     * @example unwrap([1, 2, 3])    // returns as-is
     * @example unwrap('hello')      // ['hello']
     */
    public static function unwrap(mixed $value): array
    {
        return $value instanceof static ? $value->all() : (array) $value;
    }

    /**
     * Retrieve a value by dot-notation path.
     *
     * - No path or null  -> returns all items (same as all())
     * - Plain path       -> returns the value or $default
     * - Wildcard path    -> returns an array preserving parent keys;
     *                      nested wildcards produce nested arrays
     *
     * @example get('user.name')
     * @example get('users.*.name')
     * @example get('users.*.emails.*.active', false)
     */
    public function get(?string $path = null, mixed $default = null): mixed
    {
        if ($path === null || $path === '') {
            return $this->items;
        }

        $sentinel = self::sentinel();
        $result   = $this->resolveGet($this->items, $this->parsePath($path), $sentinel);

        return $result === $sentinel ? $default : $result;
    }

    /**
     * Return the value at $path if it exists; otherwise set it to $default and return that.
     *
     * $default may be a callable — it is invoked only when the path is missing,
     * and its return value is stored and returned.
     *
     * @example getOrPut('cache.token', fn() => generateToken())
     * @example getOrPut('meta.retries', 0)
     */
    public function getOrPut(string $path, mixed $default): mixed
    {
        $sentinel = self::sentinel();
        $result   = $this->resolveGet($this->items, $this->parsePath($path), $sentinel);

        if ($result !== $sentinel) {
            return $result;
        }

        $value = is_callable($default) ? $default() : $default;
        $this->set($path, $value);

        return $value;
    }

    /**
     * Set a value at the given dot-notation path.
     *
     * - Plain path    -> sets the value, creating intermediate arrays as needed
     * - Wildcard path -> sets the value on every matching node
     *
     * @example set('user.name', 'Alice')
     * @example set('users.*.active', true)
     */
    public function set(string $path, mixed $value = null): static
    {
        $this->resolveSet($this->items, $this->parsePath($path), $value);

        return $this;
    }

    /**
     * Check whether a dot-notation path exists and is not null.
     *
     * - Plain path    -> true if the key chain exists
     * - Wildcard path -> true if at least one matching node exists
     *
     * Use hasKey() if you need to distinguish null values from missing keys.
     *
     * @example has('user.name')
     * @example has('users.*.email')
     */
    public function has(string $path): bool
    {
        return $this->resolveHas($this->items, $this->parsePath($path), self::sentinel());
    }

    /**
     * Check whether a path exists (key is present, even if value is null).
     *
     * @example hasKey('user.nickname')
     */
    public function hasKey(string $path): bool
    {
        return $this->resolveHasKey($this->items, $this->parsePath($path), self::sentinel());
    }

    /**
     * Check whether at least one of the given dot-notation paths exists and is not null.
     *
     * @example hasAny('email', 'phone')
     * @example hasAny('user.name', 'user.email')
     */
    public function hasAny(string ...$paths): bool
    {
        foreach ($paths as $path) {
            if ($this->has($path)) {
                return true;
            }
        }

        return false;
    }

    /**
     * Check whether ALL of the given dot-notation paths exist and are not null.
     *
     * @example hasAll('id', 'name', 'email')
     * @example hasAll('user.id', 'user.email')
     */
    public function hasAll(string ...$paths): bool
    {
        foreach ($paths as $path) {
            if (!$this->has($path)) {
                return false;
            }
        }

        return true;
    }

    /**
     * Remove the element at the given dot-notation path.
     *
     * - Plain path    -> unsets the key
     * - Wildcard path -> unsets the matching key in every parent node
     *
     * @example remove('user.token')
     * @example remove('users.*.password')
     */
    public function remove(string $path): static
    {
        $this->resolveRemove($this->items, $this->parsePath($path));

        return $this;
    }

    /**
     * Alias for remove() — remove an item by dot-notation path.
     *
     * @example forget('user.name')
     * @example forget('items.0')
     */
    public function forget(string $path): static
    {
        return $this->remove($path);
    }

    /**
     * Count elements.
     *
     * - No path -> count of top-level items (satisfies Countable)
     * - With path -> count of the array at that path; scalars count as 1,
     *               missing paths count as 0
     *
     * @example count()
     * @example count('users')
     * @example count('users.0.emails')
     */
    #[\Override]
    public function count(?string $path = null): int
    {
        if ($path === null) {
            return count($this->items);
        }

        $value = $this->get($path);

        return match (true) {
            $value === null          => 0,
            is_array($value)         => count($value),
            is_countable($value)     => count($value),
            default                  => 1,
        };
    }

    /**
     * Return all items as a plain array.
     */
    public function all(): array
    {
        return $this->items;
    }

    /**
     * Remove all items from the collection.
     */
    public function clear(): static
    {
        $this->items = [];

        return $this;
    }

    /**
     * Push a value onto the end of the top-level array.
     */
    public function push(mixed $value): static
    {
        $this->items[] = $value;

        return $this;
    }

    /**
     * Merge an array or another Collection into this collection (top-level).
     */
    public function merge(array|Arrayable $items): static
    {
        $this->items = array_merge(
            $this->items,
            $items instanceof Arrayable ? $items->all() : $items,
        );

        return $this;
    }

    /**
     * Merge arrays recursively (array_merge_recursive) into this collection.
     *
     * Unlike merge(), repeated string keys produce nested arrays rather than
     * overwriting each other.
     *
     * @example mergeRecursive(['meta' => ['a' => 1]])  // deep-merges 'meta'
     */
    public function mergeRecursive(array|Arrayable $items): static
    {
        $data        = $items instanceof Arrayable ? $items->all() : $items;
        $this->items = array_merge_recursive($this->items, $data);

        return $this;
    }

    /**
     * Replace values using array_replace semantics (integer keys are significant).
     *
     * Unlike merge(), existing items are replaced by matching keys from $items;
     * missing keys in $items leave the original values untouched.
     *
     * @example replace([0 => 'x'])        // replaces index 0
     * @example replace(['name' => 'Bob'])  // replaces 'name' key
     */
    public function replace(array|Arrayable $items): static
    {
        $data = $items instanceof Arrayable ? $items->all() : $items;

        return new static(array_replace($this->items, $data), $this->wildcard, $this->delimiter);
    }

    /**
     * Recursively replace values, diving into nested arrays.
     *
     * @example replaceRecursive(['user' => ['name' => 'Bob']])
     */
    public function replaceRecursive(array|Arrayable $items): static
    {
        $data = $items instanceof Arrayable ? $items->all() : $items;

        return new static(array_replace_recursive($this->items, $data), $this->wildcard, $this->delimiter);
    }

    /**
     * Append all values from $items, always re-indexing the appended portion.
     *
     * Unlike merge(), numeric keys in $items never overwrite existing ones —
     * they are appended as new sequential entries.
     *
     * @example concat(['d', 'e'])  // appends 'd' and 'e' to the collection
     */
    public function concat(array|Arrayable $items): static
    {
        $data        = array_values($items instanceof Arrayable ? $items->all() : $items);
        $this->items = array_merge($this->items, $data);

        return $this;
    }

    /**
     * Fill in keys that are missing from this collection using $items.
     *
     * Existing keys are never overwritten (PHP's + operator semantics).
     * Returns a new collection.
     *
     * @example union(['a' => 1, 'b' => 99])  // adds 'b' only if not present
     */
    public function union(array|Arrayable $items): static
    {
        $data = $items instanceof Arrayable ? $items->all() : $items;

        return new static($this->items + $data, $this->wildcard, $this->delimiter);
    }

    /**
     * Set a key/path to $value (readable alias for set()).
     *
     * @example put('name', 'Alice')
     * @example put('user.email', 'a@b.c')
     */
    public function put(string $key, mixed $value): static
    {
        return $this->set($key, $value);
    }

    /**
     * Return a new Collection of items satisfying a callback, or — when a
     * dot-notation path is given — items where the value at that path matches
     * $value (strict) or is truthy when $value is omitted.
     *
     * @example filter(fn($u) => $u['active'])
     * @example filter('active')            // items where active is truthy
     * @example filter('role', 'admin')     // items where role === 'admin'
     */
    public function filter(string|callable $pathOrCallback, mixed $value = null): static
    {
        if (!is_string($pathOrCallback)) {
            return new static(
                array_values(array_filter($this->items, $pathOrCallback)),
                $this->wildcard,
                $this->delimiter,
            );
        }

        $exactMatch = func_num_args() >= 2;
        $sentinel   = self::sentinel();
        $segments   = $this->parsePath($pathOrCallback);

        return new static(
            array_values(array_filter($this->items, function ($item) use ($value, $exactMatch, $segments, $sentinel) {
                if (!is_array($item)) {
                    return false;
                }
                $found = $this->resolveGet($item, $segments, $sentinel);
                if ($found === $sentinel) {
                    return false;
                }

                return $exactMatch ? $found === $value : (bool) $found;
            })),
            $this->wildcard,
            $this->delimiter,
        );
    }

    /**
     * Return a new Collection of items that do NOT satisfy the callback or path criteria.
     *
     * Accepts the same argument forms as filter():
     *   reject(callable $callback)
     *   reject(string $path)            // items where path value is falsy
     *   reject(string $path, $value)    // items where path !== $value (strict)
     *
     * @example reject(fn($u) => $u['active'])
     * @example reject('active')
     * @example reject('role', 'guest')
     */
    public function reject(string|callable $pathOrCallback, mixed $value = null): static
    {
        if (!is_string($pathOrCallback)) {
            return new static(
                array_values(array_filter($this->items, fn($item) => !$pathOrCallback($item))),
                $this->wildcard,
                $this->delimiter,
            );
        }

        $exactMatch = func_num_args() >= 2;
        $sentinel   = self::sentinel();
        $segments   = $this->parsePath($pathOrCallback);

        return new static(
            array_values(array_filter($this->items, function ($item) use ($value, $exactMatch, $segments, $sentinel) {
                if (!is_array($item)) {
                    return true;
                }
                $found = $this->resolveGet($item, $segments, $sentinel);
                if ($found === $sentinel) {
                    return true;
                }

                return $exactMatch ? $found !== $value : !(bool) $found;
            })),
            $this->wildcard,
            $this->delimiter,
        );
    }

    /**
     * Return a new Collection with the callback applied to every top-level item.
     */
    public function map(callable $callback): static
    {
        return new static(
            array_map($callback, $this->items),
            $this->wildcard,
            $this->delimiter,
        );
    }

    /**
     * Map items and remove results that are null or false — in a single pass.
     *
     * Equivalent to map()->filter() but more efficient and intent-revealing.
     * Values of 0, '', [] and true are kept — only null and false are discarded.
     *
     * @example filterMap(fn($u) => $u['active'] ? $u['email'] : null)
     * @example filterMap(fn($n) => $n % 2 === 0 ? $n * 10 : false)
     */
    public function filterMap(callable $callback): static
    {
        $result = [];

        foreach ($this->items as $key => $item) {
            $mapped = $callback($item, $key);
            if ($mapped !== null && $mapped !== false) {
                $result[] = $mapped;
            }
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Iterate each item, spreading its sub-array values as separate arguments.
     *
     * Useful when items are fixed-length arrays (e.g. coordinate pairs, CSV rows).
     * Return false from the callback to stop iteration early.
     *
     * @example eachSpread(fn($name, $age) => print "$name is $age")
     */
    public function eachSpread(callable $callback): static
    {
        foreach ($this->items as $item) {
            if ($callback(...(array) $item) === false) {
                break;
            }
        }

        return $this;
    }

    /**
     * Map each item by spreading its sub-array values as separate arguments.
     *
     * @example mapSpread(fn($x, $y) => $x + $y)
     */
    public function mapSpread(callable $callback): static
    {
        return $this->map(fn($item) => $callback(...(array) $item));
    }

    /**
     * Map each item into a new instance of $class.
     *
     * The item is passed as the first (and only) constructor argument.
     *
     * @example mapInto(Money::class)  // new Money($item) for each item
     */
    public function mapInto(string $class): static
    {
        return $this->map(fn($item) => new $class($item));
    }

    /**
     * Map items to [$key => $value] pairs and group by $key.
     *
     * Each callback invocation must return a single-element associative array.
     * All values sharing the same key are collected into a sub-Collection.
     *
     * @example mapToGroups(fn($item) => [$item['department'] => $item['name']])
     */
    public function mapToGroups(callable $callback): static
    {
        $groups = [];

        foreach ($this->items as $key => $item) {
            $pair      = $callback($item, $key);
            $groupKey  = array_key_first($pair);
            $groups[$groupKey][] = $pair[$groupKey];
        }

        foreach ($groups as $k => $values) {
            $groups[$k] = new static($values, $this->wildcard, $this->delimiter);
        }

        return new static($groups, $this->wildcard, $this->delimiter);
    }

    /**
     * Map items to key-value pairs using a callback that returns [$key => $value].
     *
     * Useful for building lookup maps or re-shaping data.
     *
     * @example mapWithKeys(fn($u) => [$u['id'] => $u['name']])
     * @example mapWithKeys(fn($u) => [$u['email'] => $u])
     */
    public function mapWithKeys(callable $callback): static
    {
        $result = [];

        foreach ($this->items as $key => $item) {
            $pair = $callback($item, $key);
            if (is_array($pair)) {
                foreach ($pair as $k => $v) {
                    $result[$k] = $v;
                }
            }
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Transform only the keys of the collection, preserving values.
     *
     * The callback receives ($key, $value) and must return the new key.
     *
     * @example mapKeys(fn($k) => strtolower($k))
     * @example mapKeys(fn($k, $v) => $v['id'])
     */
    public function mapKeys(callable $callback): static
    {
        $result = [];

        foreach ($this->items as $key => $value) {
            $result[$callback($key, $value)] = $value;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Transform (mutate) the collection in-place by applying $callback to every item.
     *
     * Unlike map(), this modifies the existing collection rather than creating
     * a new one. Use map() if you need immutability.
     *
     * @example transform(fn($v) => $v * 2)
     */
    public function transform(callable $callback): static
    {
        $this->items = array_map($callback, $this->items);

        return $this;
    }

    /**
     * Return a new Collection with items sorted by the callback or naturally.
     */
    public function sort(?callable $callback = null): static
    {
        $items = $this->items;

        $callback === null ? sort($items) : usort($items, $callback);

        return new static($items, $this->wildcard, $this->delimiter);
    }

    /**
     * Return a new Collection with keys sorted by the callback or naturally.
     */
    public function ksort(?callable $callback = null): static
    {
        $items = $this->items;

        $callback === null ? ksort($items) : uksort($items, $callback);

        return new static($items, $this->wildcard, $this->delimiter);
    }

    /**
     * Return a new Collection sorted in descending order.
     *
     * @example sortDesc()
     */
    public function sortDesc(?callable $callback = null): static
    {
        return $this->sort($callback)->reverse();
    }

    /**
     * Return a new Collection with keys sorted ascending (alias for ksort()).
     *
     * @example sortKeys()
     */
    public function sortKeys(?callable $callback = null): static
    {
        return $this->ksort($callback);
    }

    /**
     * Return a new Collection with keys sorted in descending order.
     *
     * @example sortKeysDesc()
     */
    public function sortKeysDesc(): static
    {
        $items = $this->items;
        krsort($items);

        return new static($items, $this->wildcard, $this->delimiter);
    }

    /**
     * Return a new Collection with keys sorted using a custom comparator.
     *
     * The comparator receives two keys and must return < 0, 0, or > 0.
     * Useful for locale-aware, natural-order, or domain-specific key sorting.
     *
     * @example sortKeysUsing('strnatcasecmp')
     * @example sortKeysUsing(fn($a, $b) => strlen($a) <=> strlen($b))
     */
    public function sortKeysUsing(callable $comparator): static
    {
        $items = $this->items;
        uksort($items, $comparator);

        return new static($items, $this->wildcard, $this->delimiter);
    }

    /**
     * Determine whether the collection is empty.
     */
    public function isEmpty(): bool
    {
        return $this->items === [];
    }

    /**
     * Determine whether the collection is not empty.
     */
    public function isNotEmpty(): bool
    {
        return $this->items !== [];
    }

    /**
     * Determine whether the items form a list (consecutive integer keys starting at 0).
     *
     * Equivalent to array_is_list().
     *
     * @example isList()  // true for [1, 2, 3], false for ['a' => 1]
     */
    public function isList(): bool
    {
        return array_is_list($this->items);
    }

    /**
     * Determine whether the items are an associative array (not a list).
     *
     * @example isAssoc()  // true for ['a' => 1], false for [1, 2, 3]
     */
    public function isAssoc(): bool
    {
        return !array_is_list($this->items);
    }

    /**
     * Return the first top-level item, or invoke $callback to find the first match.
     */
    public function first(?callable $callback = null, mixed $default = null): mixed
    {
        if ($callback === null) {
            return $this->items === [] ? $default : reset($this->items);
        }

        return array_find($this->items, $callback) ?? $default;
    }

    /**
     * Return the last top-level item, or invoke $callback to find the last match.
     *
     * @example last()                       // last item
     * @example last(fn($u) => $u['active']) // last active user
     */
    public function last(?callable $callback = null, mixed $default = null): mixed
    {
        if ($callback === null) {
            return $this->items === [] ? $default : end($this->items);
        }

        $result = null;
        $found  = false;

        foreach ($this->items as $key => $item) {
            if ($callback($item, $key)) {
                $result = $item;
                $found  = true;
            }
        }

        return $found ? $result : $default;
    }

    /**
     * Return the first item (optionally matching $callback), or throw if none found.
     *
     * @throws \RuntimeException when no item matches
     *
     * @example firstOrFail()
     * @example firstOrFail(fn($u) => $u['active'])
     */
    public function firstOrFail(?callable $callback = null): mixed
    {
        $sentinel = self::sentinel();

        if ($callback === null) {
            $result = $this->items === [] ? $sentinel : reset($this->items);
        } else {
            $result = $sentinel;
            foreach ($this->items as $key => $item) {
                if ($callback($item, $key)) {
                    $result = $item;
                    break;
                }
            }
        }

        if ($result === $sentinel) {
            throw new \RuntimeException('The collection contains no matching items.');
        }

        return $result;
    }

    /**
     * Return the first item where the value at $path matches the criteria.
     *
     * Supports the same operator syntax as where().
     * Returns null when nothing matches.
     *
     * @example firstWhere('active')
     * @example firstWhere('role', 'admin')
     * @example firstWhere('age', '>=', 18)
     */
    public function firstWhere(string $path, mixed $operatorOrValue = null, mixed $value = null): mixed
    {
        return match (func_num_args()) {
            1       => $this->filter($path)->first(),
            2       => $this->where($path, '==', $operatorOrValue)->first(),
            default => $this->where($path, $operatorOrValue, $value)->first(),
        };
    }

    /**
     * Return the key of the first item, or the key of the first item satisfying $callback.
     *
     * Returns null when no item matches.
     *
     * @example firstKey()                          // key of first item
     * @example firstKey(fn($v) => $v['active'])    // key of first active item
     */
    public function firstKey(?callable $callback = null): string|int|null
    {
        if ($callback === null) {
            return array_key_first($this->items);
        }

        foreach ($this->items as $key => $item) {
            if ($callback($item, $key)) {
                return $key;
            }
        }

        return null;
    }

    /**
     * Return the key of the last item, or the key of the last item satisfying $callback.
     *
     * Returns null when no item matches.
     *
     * @example lastKey()                           // key of last item
     * @example lastKey(fn($v) => $v['active'])     // key of last active item
     */
    public function lastKey(?callable $callback = null): string|int|null
    {
        if ($callback === null) {
            return array_key_last($this->items);
        }

        $result = null;

        foreach ($this->items as $key => $item) {
            if ($callback($item, $key)) {
                $result = $key;
            }
        }

        return $result;
    }

    /**
     * Get the value at $path from the first item in the collection.
     *
     * @example value('user.email')
     * @example value('price', 0.0)
     */
    public function value(string $path, mixed $default = null): mixed
    {
        $first = $this->first();

        if (!is_array($first)) {
            return $default;
        }

        $result = $this->resolveGet($first, $this->parsePath($path), self::sentinel());

        return $result === self::sentinel() ? $default : $result;
    }

    /**
     * Return the item immediately after the given value or the item where the callback first returns true.
     *
     * @example after(3)                             // item after value 3
     * @example after(fn($u) => $u['id'] === 2)     // item after id=2
     */
    public function after(mixed $valueOrCallback, mixed $default = null): mixed
    {
        $found = false;

        foreach ($this->items as $key => $item) {
            if ($found) {
                return $item;
            }
            $found = is_callable($valueOrCallback)
                ? (bool) $valueOrCallback($item, $key)
                : ($item === $valueOrCallback);
        }

        return $default;
    }

    /**
     * Return the item immediately before the given value or the item where the callback first returns true.
     *
     * @example before(3)                             // item before value 3
     * @example before(fn($u) => $u['id'] === 2)     // item before id=2
     */
    public function before(mixed $valueOrCallback, mixed $default = null): mixed
    {
        $sentinel = self::sentinel();
        $prev     = $sentinel;

        foreach ($this->items as $key => $item) {
            $match = is_callable($valueOrCallback)
                ? (bool) $valueOrCallback($item, $key)
                : ($item === $valueOrCallback);

            if ($match) {
                return $prev === $sentinel ? $default : $prev;
            }

            $prev = $item;
        }

        return $default;
    }

    /**
     * Return a new Collection of values at $path across all top-level items.
     *
     * When $key is given the result is indexed by the value at that path
     * in each item (useful for building lookup maps).
     *
     * @example pluck('name')           // [0 => 'Alice', 1 => 'Bob']
     * @example pluck('name', 'id')     // [1 => 'Alice', 2 => 'Bob']
     * @example pluck('address.city')   // [0 => 'Moscow', 1 => 'Berlin']
     */
    public function pluck(string $path, ?string $key = null): static
    {
        $sentinel = self::sentinel();
        $pathSegs = $this->parsePath($path);
        $keySegs  = $key !== null ? $this->parsePath($key) : null;
        $result   = [];

        foreach ($this->items as $index => $item) {
            if (!is_array($item)) {
                continue;
            }
            $value = $this->resolveGet($item, $pathSegs, $sentinel);
            if ($value === $sentinel) {
                continue;
            }
            if ($keySegs !== null) {
                $k = $this->resolveGet($item, $keySegs, $sentinel);
                $result[$k !== $sentinel && is_scalar($k) ? $k : $index] = $value;
            } else {
                $result[$index] = $value;
            }
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Collapse all nested arrays/Collections into a single flat indexed array.
     *
     * Without $depth (default INF) all levels are flattened.
     * Pass an integer to limit how many levels deep to descend:
     *   flatten(1) — one level deep (equivalent to collapse())
     *   flatten(2) — two levels deep, etc.
     *
     * @example flatten()    // [[1, [2, 3]], [4]] -> [1, 2, 3, 4]
     * @example flatten(1)   // [[1, [2, 3]], [4]] -> [1, [2, 3], 4]
     */
    public function flatten(int|float $depth = INF): static
    {
        return new static(
            $this->flattenValues($this->items, $depth),
            $this->wildcard,
            $this->delimiter,
