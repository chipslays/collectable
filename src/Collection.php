    /**
     * Execute $then unless the collection is empty (equivalent to whenNotEmpty).
     */
    public function unlessEmpty(callable $then, ?callable $else = null): static
    {
        return $this->unless($this->isEmpty(), $then, $else);
    }

    /**
     * Execute $then unless the collection is not-empty (equivalent to whenEmpty).
     */
    public function unlessNotEmpty(callable $then, ?callable $else = null): static
    {
        return $this->unless($this->isNotEmpty(), $then, $else);
    }

    /**
     * Pipe the collection through an ordered array of callables.
     *
     * Each callable receives the value returned by the previous one.
     * The first callable receives the Collection itself.
     *
     * @example pipeThrough([
     *     fn($c) => $c->filter('active'),
     *     fn($c) => $c->sortBy('name'),
     * ])
     */
    public function pipeThrough(array $pipes): mixed
    {
        return array_reduce(
            $pipes,
            fn($carry, $pipe) => $pipe($carry),
            $this,
        );
    }

    /**
     * Ensure every item is of the given type(s), or throw.
     *
     * Accepts PHP native types ('int', 'string', 'array', 'float', 'bool',
     * 'null', 'numeric', 'scalar') and fully-qualified class/interface names.
     * An array of types is accepted as OR logic.
     *
     * @throws \UnexpectedValueException when an item has an unexpected type
     *
     * @example ensure('int')
     * @example ensure(['int', 'float'])
     * @example ensure(User::class)
     */
    public function ensure(string|array $types): static
    {
        $types = (array) $types;

        foreach ($this->items as $item) {
            $ok = false;
            foreach ($types as $type) {
                $ok = match ($type) {
                    'int', 'integer'  => is_int($item),
                    'float', 'double' => is_float($item),
                    'string'          => is_string($item),
                    'bool', 'boolean' => is_bool($item),
                    'array'           => is_array($item),
                    'null'            => $item === null,
                    'numeric'         => is_numeric($item),
                    'scalar'          => is_scalar($item),
                    default           => $item instanceof $type,
                };
                if ($ok) {
                    break;
                }
            }

            if (!$ok) {
                $actual   = is_object($item) ? $item::class : gettype($item);
                $expected = implode('|', $types);
                throw new \UnexpectedValueException(
                    "Expected item of type [{$expected}], got [{$actual}]."
                );
            }
        }

        return $this;
    }

