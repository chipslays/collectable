        if (function_exists('dd')) {
            dd($this->items);
        } else {
            var_dump($this->items);
            exit(1);
        }
    }

    /**
     * Return a deep clone of this collection.
     */
    public function copy(): static
    {
        return clone $this;
    }

    /**
     * Execute $then when $condition is truthy, otherwise $else (when provided).
     *
     * The callback receives $this. If it returns a Collection instance that
     * value is used as the result; otherwise $this is returned, making
     * conditional transformations safely chainable.
     *
     * $condition may be a callable — it receives $this and its return value
     * is used as the boolean condition.
     *
     * @example when($isAdmin, fn($c) => $c->merge($adminItems))
     * @example when(fn($c) => $c->isNotEmpty(), fn($c) => $c->sortBy('name'))
     */
    public function when(bool|callable $condition, callable $then, ?callable $else = null): static
    {
        $value = is_callable($condition) ? $condition($this) : $condition;

        if ($value) {
            $result = $then($this);

            return $result instanceof static ? $result : $this;
        }

        if ($else !== null) {
            $result = $else($this);

            return $result instanceof static ? $result : $this;
        }

        return $this;
    }

    /**
     * Execute $then when $condition is falsy (inverse of when()).
     *
     * @example unless($c->isEmpty(), fn($c) => $c->sortBy('name'))
     */
    public function unless(bool|callable $condition, callable $then, ?callable $else = null): static
    {
        $resolved = is_callable($condition) ? (bool) $condition($this) : $condition;

        return $this->when(!$resolved, $then, $else);
    }

    /**
     * Execute $then when the collection is empty.
     *
     * Shorthand for when(isEmpty(), ...).
     */
    public function whenEmpty(callable $then, ?callable $else = null): static
    {
        return $this->when($this->isEmpty(), $then, $else);
    }

    /**
     * Execute $then when the collection is not empty.
     *
     * Shorthand for when(isNotEmpty(), ...).
