    /**
     * Determine whether ALL items pass the test.
     *
     * Accepts a callback or a dot-notation path with optional value:
     *   every(callable $callback)
     *   every(string $path)            // every item has a truthy value at $path
     *   every(string $path, $value)    // every item's path value === $value (strict)
     *
     * Returns true for an empty collection.
     *
     * @example every(fn($u) => $u['active'])
     * @example every('active')
     * @example every('role', 'admin')
     */
    public function every(string|callable $pathOrCallback, mixed $value = null): bool
    {
        if (!is_string($pathOrCallback)) {
            foreach ($this->items as $key => $item) {
                if (!$pathOrCallback($item, $key)) {
                    return false;
                }
            }

            return true;
        }

        $exactMatch = func_num_args() >= 2;
        $sentinel   = self::sentinel();
        $segments   = $this->parsePath($pathOrCallback);

        foreach ($this->items as $item) {
            if (!is_array($item)) {
                return false;
            }
            $found = $this->resolveGet($item, $segments, $sentinel);
            if ($found === $sentinel) {
                return false;
            }
            if ($exactMatch ? $found !== $value : !(bool) $found) {
                return false;
            }
        }

        return true;
    }

    /**
     * Return the key of the first item that matches $value (strict), or false.
     *
     * Accepts a callback for custom matching.
     *
     * @example search('Alice')
     * @example search(fn($v, $k) => $v['active'])
     */
    public function search(mixed $valueOrCallback, bool $strict = true): string|int|false
    {
        if (is_callable($valueOrCallback)) {
            foreach ($this->items as $key => $value) {
                if ($valueOrCallback($value, $key)) {
                    return $key;
                }
            }

            return false;
        }

        return array_search($valueOrCallback, $this->items, $strict);
    }

    /**
     * Return the first item where the value at $path strictly equals $value.
     *
     * Returns $default if no match is found.
     * This is the path-aware equivalent of first(fn($u) => $u['id'] === 42).
     *
     * @example find('id', 42)
     * @example find('user.email', 'alice@x.com')
     */
    public function find(string $path, mixed $value, mixed $default = null): mixed
    {
        $sentinel = self::sentinel();
        $segments = $this->parsePath($path);

        foreach ($this->items as $item) {
            if (!is_array($item)) {
                continue;
            }
            $found = $this->resolveGet($item, $segments, $sentinel);
            if ($found !== $sentinel && $found === $value) {
                return $item;
            }
        }

        return $default;
    }

    /**
     * Return the single item matching the criteria, or throw if zero or multiple match.
     *
     * - sole()               -> the collection must contain exactly one item
     * - sole('id', 42)       -> exactly one item where id == 42
     * - sole(callable)       -> exactly one item satisfying the callback
     *
     * @throws \UnderflowException  when no items match
     * @throws \OverflowException   when multiple items match
     *
     * @example $user = $users->sole('id', $id)
     */
    public function sole(string|callable|null $pathOrCallback = null, mixed $value = null): mixed
    {
        $argc = func_num_args();

        if ($argc === 0 || $pathOrCallback === null) {
            $filtered = $this;
        } elseif ($argc >= 2 && is_string($pathOrCallback)) {
            $filtered = $this->where($pathOrCallback, '===', $value);
        } else {
            $filtered = $this->filter($pathOrCallback);
        }

        if ($filtered->isEmpty()) {
            throw new \UnderflowException('The collection contains no matching items.');
        }

        if ($filtered->count() > 1) {
            throw new \OverflowException('The collection contains multiple matching items.');
        }

        return $filtered->first();
    }

    /**
     * Determine whether exactly one item matches the criteria (boolean variant of sole()).
     *
     * Returns false instead of throwing when zero or multiple items match.
     *
     * @example hasSole('id', 42)
     * @example hasSole(fn($u) => $u['active'])
     */
    public function hasSole(string|callable|null $pathOrCallback = null, mixed $value = null): bool
    {
        try {
            $this->sole(...func_get_args());

            return true;
        } catch (\UnderflowException | \OverflowException) {
            return false;
        }
    }

    /**
     * Return items that are duplicates of a previously seen value.
     *
     * Without $by, compares entire items by serialisation.
     * With a dot-notation path or callback, groups by that key.
     * Original keys are preserved; only the 2nd+ occurrence is included.
     *
     * @example duplicates()         // finds repeated top-level scalars/arrays
     * @example duplicates('email')  // finds users with duplicate emails
     */
    public function duplicates(string|callable|null $by = null): static
    {
        $sentinel = self::sentinel();

        if ($by === null) {
            $seen  = [];
            $dupes = [];
            foreach ($this->items as $key => $item) {
                $hash = serialize($item);
                if (array_key_exists($hash, $seen)) {
                    $dupes[$key] = $item;
                } else {
                    $seen[$hash] = true;
                }
            }

            return new static($dupes, $this->wildcard, $this->delimiter);
        }

        $callback = is_string($by)
            ? fn($item) => is_array($item)
                ? $this->resolveGet($item, $this->parsePath($by), $sentinel)
                : $sentinel
            : $by;

        $seen  = [];
        $dupes = [];

        foreach ($this->items as $key => $item) {
            $group = $callback($item, $key);
            if ($group === $sentinel) {
                continue;
            }
            $hash = serialize($group);
            if (array_key_exists($hash, $seen)) {
                $dupes[$key] = $item;
            } else {
                $seen[$hash] = true;
            }
        }

        return new static($dupes, $this->wildcard, $this->delimiter);
    }

    /**
     * Return duplicate items using strict (===) comparison.
     *
     * Unlike duplicates(), 1 and '1' are treated as different values.
     *
     * @example duplicatesStrict()           // top-level strict duplicates
     * @example duplicatesStrict('status')   // duplicates by path, strict
     */
    public function duplicatesStrict(string|callable|null $by = null): static
    {
        $sentinel = self::sentinel();

        $callback = $by === null
            ? null
            : (is_string($by)
                ? fn($item) => is_array($item)
                    ? $this->resolveGet($item, $this->parsePath($by), $sentinel)
                    : $sentinel
                : $by);

        $seen  = [];
        $dupes = [];

        foreach ($this->items as $key => $item) {
            $val = $callback !== null ? $callback($item, $key) : $item;

            if ($val === $sentinel) {
                continue;
            }

            $isDup = false;
            foreach ($seen as $s) {
                if ($s === $val) {
                    $isDup = true;
                    break;
                }
            }

            if ($isDup) {
                $dupes[$key] = $item;
            } else {
                $seen[] = $val;
            }
        }

        return new static($dupes, $this->wildcard, $this->delimiter);
    }

    /**
     * Return the sum of all items, of the plucked path values, or via a callback.
