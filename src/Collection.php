        return new $class($this);
    }

    /**
     * Pass the collection to $callback for side-effects and return $this.
     *
     * Useful for debugging in the middle of a fluent chain.
     *
     * @example tap(fn($c) => dump($c->count()))
     */
    public function tap(callable $callback): static
    {
        $callback($this);

        return $this;
    }

    /**
     * Dump the collection contents via var_dump and return $this (stays in chain).
     *
     * @example $collection->dump()->filter(...)
     */
    public function dump(): static
    {
        if (function_exists('dump')) {
            dump($this->items);
        } else {
            var_dump($this->items);
        }

        return $this;
    }

