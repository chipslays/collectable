    /**
     * Filter items where the value at $path is within [$min, $max] (inclusive).
     *
     * @param array $values  Two-element array [$min, $max].
     *
     * @example whereBetween('age', [18, 65])
     * @example whereBetween('price', [10.0, 99.99])
     */
    public function whereBetween(string $path, array $values): static
    {
        [$min, $max] = $values;

        return $this->where($path, '>=', $min)->where($path, '<=', $max);
    }

    /**
     * Filter items where the value at $path is outside [$min, $max].
     *
     * @param array $values  Two-element array [$min, $max].
     *
     * @example whereNotBetween('age', [18, 65])
     */
    public function whereNotBetween(string $path, array $values): static
    {
        [$min, $max] = $values;
        $sentinel   = self::sentinel();
        $segments   = $this->parsePath($path);

        return new static(
            array_values(array_filter($this->items, function ($item) use ($min, $max, $segments, $sentinel) {
                if (!is_array($item)) {
                    return false;
                }
                $found = $this->resolveGet($item, $segments, $sentinel);
                if ($found === $sentinel) {
                    return false;
                }

                return $found < $min || $found > $max;
            })),
            $this->wildcard,
            $this->delimiter,
        );
    }

    /**
     * Filter items by class or interface.
     *
     * Without $path, checks the item itself.
     * With a dot-notation $path, checks the value at that path.
     * Accepts a single class or an array of classes (OR logic).
     *
     * @example whereInstanceOf(User::class)
     * @example whereInstanceOf([AdminUser::class, ModUser::class])
     * @example whereInstanceOf(PaymentService::class, 'service')
     */
    public function whereInstanceOf(string|array $class, ?string $path = null): static
    {
        $classes  = (array) $class;
        $sentinel = self::sentinel();

        return new static(
            array_values(array_filter($this->items, function ($item) use ($classes, $path, $sentinel) {
                $target = $item;
                if ($path !== null) {
                    if (!is_array($item)) {
                        return false;
                    }
                    $found = $this->resolveGet($item, $this->parsePath($path), $sentinel);
                    if ($found === $sentinel) {
                        return false;
                    }
                    $target = $found;
                }

                foreach ($classes as $cls) {
                    if ($target instanceof $cls) {
                        return true;
                    }
                }

                return false;
            })),
            $this->wildcard,
            $this->delimiter,
        );
    }

    /**
     * Filter items where the string value at $path contains $needle.
     *
     * @example whereContains('name', 'ali')            // str_contains, case-sensitive
     * @example whereContains('email', '@gmail', true)  // case-insensitive
