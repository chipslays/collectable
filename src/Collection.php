    /**
     * Apply $callback to every item and collapse the results one level.
     *
     * @example flatMap(fn($u) => $u['emails'])
     */
    public function flatMap(callable $callback): static
    {
        return $this->map($callback)->collapse();
    }

    /**
     * Perform a running fold (like reduce), returning every intermediate value.
     *
     * The result collection has the same count as the input collection.
     * Useful for running totals, cumulative sums, progressive percentages.
     *
     * @example Collection::make([1, 2, 3, 4])->scan(fn($carry, $v) => $carry + $v, 0)
     *          // [1, 3, 6, 10]
     */
    public function scan(callable $callback, mixed $initial = null): static
    {
        $result = [];
        $carry  = $initial;

        foreach ($this->items as $key => $item) {
            $carry    = $callback($carry, $item, $key);
            $result[] = $carry;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Map only the values at a dot-notation path inside each item, leaving the rest intact.
     *
     * Supports nested dot-notation and wildcard paths via the existing path engine.
     *
     * @example mapPath('price', fn($p) => round($p * 1.2, 2))
     * @example mapPath('meta.slug', fn($s) => strtolower($s))
     * @example mapPath('tags.*.name', fn($n) => strtoupper($n))  // wildcard
     */
    public function mapPath(string $path, callable $callback): static
    {
        $sentinel = self::sentinel();
        $segments = $this->parsePath($path);

        $result = [];
        foreach ($this->items as $key => $item) {
            if (!is_array($item)) {
                $result[$key] = $item;
                continue;
            }

            $current = $this->resolveGet($item, $segments, $sentinel);
            if ($current === $sentinel) {
                $result[$key] = $item;
                continue;
            }

            $copy = $item;
            $this->resolveSet($copy, $segments, $callback($current, $key));
            $result[$key] = $copy;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Apply different transformations to different paths in a single pass.
     *
     * Each key in $transformers is a dot-notation path; each value is a callable
     * (or a callable string like 'strtolower') that receives the current value.
     * Equivalent to chaining multiple mapPath() calls, but more readable.
     *
     * @example evolve([
     *     'price'     => fn($p) => round($p * 1.2, 2),
     *     'meta.slug' => 'strtolower',
     *     'name'      => fn($n) => trim($n),
     * ])
     */
    public function evolve(array $transformers): static
    {
        $result = $this;
        foreach ($transformers as $path => $fn) {
            // Wrap to pass only the value — lets callers use native functions
            // like 'trim' or 'strtolower' that accept exactly one argument.
            $result = $result->mapPath((string) $path, fn($value) => $fn($value));
        }

        return $result;
    }

    /**
     * Transpose a matrix (collection of equal-length arrays).
     *
     * Rows become columns and columns become rows.
     * Missing cells are filled with null.
     *
     * @example Collection::make([[1, 2, 3], [4, 5, 6]])->transpose()
     *          // [[1, 4], [2, 5], [3, 6]]
     */
    public function transpose(): static
    {
        if ($this->items === []) {
            return new static([], $this->wildcard, $this->delimiter);
        }

        $rows   = array_map('array_values', $this->items);
        $width  = max(array_map('count', $rows));
        $result = [];

        for ($col = 0; $col < $width; $col++) {
            $column = [];
            foreach ($rows as $row) {
                $column[] = $row[$col] ?? null;
            }
            $result[] = $column;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Build a nested tree from a flat list of nodes.
     *
     * Nodes are matched by $idKey and linked via $parentKey.
     * Root nodes are those whose $parentKey value equals $rootId (default null).
     * Children are stored under $childrenKey on each parent node.
     *
     * @example $categories->toTree('id', 'parent_id')
     * @example $nodes->toTree('id', 'parent_id', 'children', 0)
     */
    public function toTree(
        string $idKey,
        string $parentKey,
        string $childrenKey = 'children',
        mixed  $rootId      = null,
    ): static {
        $sentinel = self::sentinel();
        $idSegs   = $this->parsePath($idKey);
        $pidSegs  = $this->parsePath($parentKey);

        // Index all nodes by their id; attach an empty children array
        $map = [];
        foreach ($this->items as $item) {
            if (!is_array($item)) {
                continue;
            }
            $id = $this->resolveGet($item, $idSegs, $sentinel);
            if ($id === $sentinel) {
                continue;
            }
            $item[$childrenKey] = [];
            $map[$id]           = $item;
        }

        $roots = [];
        foreach ($map as $id => $node) {
            $pid = $this->resolveGet($node, $pidSegs, $sentinel);

            if ($pid === $sentinel || $pid === $rootId) {
                $roots[] = &$map[$id];
            } elseif (array_key_exists($pid, $map)) {
                $map[$pid][$childrenKey][] = &$map[$id];
            }
        }

        return new static(array_values($roots), $this->wildcard, $this->delimiter);
    }

    /**
     * Collapse all nested arrays (or Collections) into a single flat array.
     *
     * @example Collection::make([[1, 2], [3, 4]])->collapse()->all()  // [1,2,3,4]
     */
    public function collapse(): static
    {
        $result = [];
        foreach ($this->items as $item) {
            if ($item instanceof self) {
                array_push($result, ...$item->all());
            } elseif (is_array($item)) {
                array_push($result, ...$item);
            } else {
                $result[] = $item;
            }
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Convert dot-notation keys back to a nested array (inverse of flatten()).
     *
     * @example Collection::make(['user.name' => 'Alice', 'user.age' => 30])->undot()
     *          // ['user' => ['name' => 'Alice', 'age' => 30]]
     */
    public function undot(): static
    {
        $result = [];
        foreach ($this->items as $key => $value) {
            $this->resolveSet($result, $this->parsePath((string) $key), $value);
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Return a new Collection with every $step-th item, starting at $offset.
     *
     * @example nth(2)      // 1st, 3rd, 5th, …
     * @example nth(3, 1)   // 2nd, 5th, 8th, …
     */
    public function nth(int $step, int $offset = 0): static
    {
        $result = [];
        $i      = 0;
        foreach ($this->items as $item) {
            if ($i >= $offset && ($i - $offset) % $step === 0) {
                $result[] = $item;
            }
            $i++;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Pad the collection to $size with $value.
     *
     * A negative $size pads at the beginning.
     *
     * @example pad(5, 0)    // [1, 2, 0, 0, 0]
     * @example pad(-5, 0)   // [0, 0, 0, 1, 2]
     */
    public function pad(int $size, mixed $value = null): static
    {
        return new static(array_pad($this->items, $size, $value), $this->wildcard, $this->delimiter);
    }

    /**
     * Repeat every item in the collection $times times.
     *
     * @example Collection::make([1, 2])->multiply(3)->all()  // [1,2,1,2,1,2]
     */
    public function multiply(int $times): static
    {
        if ($times <= 0) {
            return new static([], $this->wildcard, $this->delimiter);
        }

        $result = [];
        for ($i = 0; $i < $times; $i++) {
            array_push($result, ...array_values($this->items));
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Cross-join this collection with one or more arrays (cartesian product).
     *
     * @example Collection::make([1, 2])->crossJoin(['a', 'b'])
     *          // [[1,'a'], [1,'b'], [2,'a'], [2,'b']]
     */
    public function crossJoin(array ...$arrays): static
    {
        $result = array_map(fn($item) => [$item], array_values($this->items));

        foreach ($arrays as $arr) {
            $expanded = [];
            foreach ($result as $partial) {
                foreach ($arr as $item) {
                    $expanded[] = [...$partial, $item];
                }
            }
            $result = $expanded;
        }

        return new static($result, $this->wildcard, $this->delimiter);
    }

    /**
     * Combine this collection (used as keys) with $values.
     *
     * @example Collection::make(['a', 'b'])->combine([1, 2])->all()
     *          // ['a' => 1, 'b' => 2]
     */
    public function combine(array|Arrayable $values): static
    {
        $vals = $values instanceof Arrayable ? array_values($values->all()) : array_values($values);

        return new static(
            array_combine(array_values($this->items), $vals),
            $this->wildcard,
            $this->delimiter,
        );
    }

    /**
